class Dots:
    """Generates pellets on the map and does food calculations"""

    def __init__(self, screen, pac):
        self.screen, self.pac = screen, pac
        self.begin = True
        self.frightState = False

    def __generateDots(self):
        for i in range(28):
            for j in range(31):
                if self.currentPellets[j][i] > 0:
                    dots = pygame.Surface((TILESIZE, TILESIZE), pygame.SRCALPHA)
                    pygame.draw.circle(dots, YELLOW, (TILESIZE//2,  TILESIZE//2), self.currentPellets[j][i]*2)
                    self.screen.blit(dots, (PADDING+i*TILESIZE, PADDING+j*TILESIZE))

    def render(self):
        if self.begin:
            self.currentPellets = PELLETBOARD
            self.begin = False
        else:
            output = self.__checkEat()
        self.__generateDots()

    def reset(self):
        self.begin = True

    def enableFrightened(self):
        return self.frightState

    def __checkEat(self):
        grid = [self.pac.loc[0]//TILESIZE, self.pac.loc[1]//TILESIZE]
        if grid == [28, 14] or grid == [-1, 14]:
            return True
        elif grid == [28, 13] or grid == [-1, 13] or grid == [28, 15] or grid == [-1, 15]:
            return False
        self.frightState = False
        if self.pac.isCentered() and self.currentPellets[self.pac.loc[1]//TILESIZE][self.pac.loc[0]//TILESIZE] > 0:
            if self.currentPellets[self.pac.loc[1]//TILESIZE][self.pac.loc[0]//TILESIZE] == 2:
                self.frightState = True
            self.currentPellets[self.pac.loc[1]//TILESIZE][self.pac.loc[0]//TILESIZE] = 0
            self.pac.pellets += 1
