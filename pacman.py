def drawGrid():
    OVERLAY = pygame.Surface((TILESIZE, TILESIZE), pygame.SRCALPHA)
    for x in range(0, WINDOW_WIDTH, TILESIZE):
        for y in range(0, WINDOW_HEIGHT, TILESIZE):
            match gameboard[math.floor(y/TILESIZE)][math.floor(x/TILESIZE)]:
                case 1:
                    OVERLAY.set_alpha(50)
                    OVERLAY.fill(GREEN)
                    print(str(math.floor(x/TILESIZE))+' '+str(math.floor(y/TILESIZE))+':'+str(gameboard[math.floor(y/WINDOW_WIDTH)][math.floor(x/WINDOW_HEIGHT)]))
                    print("GAE")
                case default:
                    OVERLAY.set_alpha(0)
                    OVERLAY.fill(RED)
            SCREEN.blit(OVERLAY,(x,y))

def start():
    global SCREEN, OVERLAY, CLOCK
    pygame.init()
    SCREEN = pygame.display.set_mode((WINDOW_WIDTH, WINDOW_HEIGHT))
    pygame.display.set_caption('PACMAN')
    CLOCK = pygame.time.Clock()
    SCREEN.fill(BLACK)
    map = pygame.transform.scale(pygame.image.load('Assets/maze.png'), (WINDOW_WIDTH,WINDOW_HEIGHT))
    SCREEN.blit(map,(0,0))
    drawGrid()
    print("Start Sequence")

def update():
    # print("Update Sequence")
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()
    pygame.display.update()

#MAIN
def main():
    print("Main Sequence")
    start()
    while True:
        update()


main()